    public void cargarTablaCatalogos(JTable t){
    DefaultTableModel m=(DefaultTableModel) t.getModel();
    t.setDefaultRenderer(Object.class, new TablaImagenes());
    m.setRowCount(0);
    
    meto=new MetodosSql();
    Catalogos vo=new Catalogos();
    ArrayList<Catalogos> list = meto.ListaCatalogos();
        if (list.size()>0) {
            for(int i=0; i<list.size(); i++){
            Object[] fila=new Object[3];
            vo=list.get(i);
            fila[1]=vo.getId();
            fila[2]=vo.getNombre();
            try{
            byte[] recuperar=vo.getFoto();
            BufferedImage img=null;
            InputStream in = new ByteArrayInputStream(recuperar);
            img=ImageIO.read(in);        
            ImageIcon icono=new ImageIcon(img.getScaledInstance(300, 350, 0));
            fila[0]=new JLabel(icono);
            }catch(Exception e){
            fila[0]="No imagen";
            }
            m.addRow(fila);
        }
        t.setModel(m);
        t.setRowHeight(350);
            
        }
    }   
    
