    private void btnPedirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPedirActionPerformed
        DefaultTableModel lista=(DefaultTableModel)tblLista.getModel();
        if (lista.getRowCount()==0) {
            JOptionPane.showMessageDialog(null, "Ingrese productos a la lista");
        }else{
            for (int i = 0; i < lista.getRowCount(); i++) {
                Date fechaPedido=jFecha.getDate();
                SimpleDateFormat fN=new SimpleDateFormat("YYYY-MM-dd");
                String f=fN.format(fechaPedido);

                SimpleDateFormat fH=new SimpleDateFormat("HH:mm:ss");
                String h=fN.format(fechaPedido);

                int idCliente=Integer.parseInt(lista.getValueAt(i, 0).toString());
                String idCalzado=lista.getValueAt(i, 1).toString();
                String nombre=lista.getValueAt(i, 2).toString();
                String apellidoP=lista.getValueAt(i, 3).toString();
                String apellidoM=lista.getValueAt(i, 4).toString();
                String color=lista.getValueAt(i, 5).toString();
                float talla=Float.parseFloat(lista.getValueAt(i, 6).toString());
                float precio=Float.parseFloat(lista.getValueAt(i, 7).toString());
                String tipoCalz=lista.getValueAt(i, 8).toString();
                String user=usuario.getText();

                meto.InsertarPedido(idCliente, idCalzado, nombre, apellidoP, apellidoM, color, talla, precio, f, h, tipoCalz,user);

                meto.InsertarEstatus(meto.BuscarIdPedido(), idCliente, nombre, apellidoP, apellidoM, precio, 0);
                
                lista.removeRow(lista.getRowCount());
            }
            meto.cargarTablaPedidos(tblTablaP);
            PanelPedidos.setVisible(true);
            PanelClientes.setVisible(false);
            PanelEstatus.setVisible(false);
            PanelCatalago.setVisible(false);
            PanelCAbonos.setVisible(false);

            btnAgregar.setVisible(false);
            btnAutorizar.setVisible(false);
            btnAgregarP.setVisible(false);
            btnAbonar.setVisible(false);
            jPanelE.setBackground(new java.awt.Color(204,204,255));
            jPanelBotonP.setBackground(new java.awt.Color(200,200,200));
            JTitulo.setText("Pedidos");

        }
    }//GEN-LAST:event_btnPedirActionPerformed

