                SimpleDateFormat fN=new SimpleDateFormat("YYYY-MM-dd");
                String f=fN.format(fechaNacimiento);
                
                String calle=txtCalle.getText();
                String telefono=txtTelefono.getText();
                String CP=txtCodigoP.getText();
                
                meto.InsertarCliente(nombre, apellidoP, apellidoM, calle, telefono, f, CP);
               
